namespace P11_Overloading
{
    public class MathOperation
    {
        
        public int Sum(int a, int b)
        {
            return a + b;
        }
        public double Sum(double a, double b)
        {
            return a + b;
        }
        public int Sum(char a, char b)
        {
            return a + b;
        }
    }
}
